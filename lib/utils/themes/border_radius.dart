abstract class AppBorderRadius {
  /// Small radius (4pt) : used for buttons, small containers, etc.
  static const double small = 4.0;

  /// Medium radius (8pt) : used for cards, dialogs, etc.
  static const double medium = 8.0;

  /// Large radius (16pt) : used for larger containers, modals, etc.
  static const double large = 16.0;

  /// Circular radius (50% of width/height) : used for circles, avatars, etc.
  static const double circular = 100.0;

  /// Custom large radius (24pt) : used for custom design elements.
  static const double customLarge = 24.0;
}
